package homework.day2.basetask;

public class TrainMethodsPrimitive {
    public void printInt(int x) {
        System.out.println("As an input I received number " + x);
    }

    public void printLong(long y) {

        System.out.println("As an input I received a long number " + y);
    }

    public void printChar(char ch) {

        System.out.println("As an input I received a char " + ch);
    }

    public void printFloat(float f) {

        System.out.println("As an input I received a float number " + f);
    }

    public void printDouble(double d) {

        System.out.println("As an input I received a double number " + d);
    }

    public void printShort(short sh) {

        System.out.println("As an input I received a short number " + sh);
    }

    public void printByte(byte b) {

        System.out.println("As an input I received a byte number " + b);
    }

    public void printBoolean(boolean boo) {

        System.out.println("As an input I received a boolean value " + boo);
    }
}
