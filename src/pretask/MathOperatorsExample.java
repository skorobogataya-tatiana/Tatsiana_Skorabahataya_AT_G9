package pretask;

public class MathOperatorsExample {
    public static void main(String[] args) {
        int x = 518;
        int y = 66;
        System.out.println("Sum of x and y is: " + (x + y));
        System.out.println("The result of subtracting y from x is: " + (x - y));
        System.out.println("The result of multiplying y from x is: " + (y * x));
        System.out.println("The result of dividing x by y is: " + (x / y));
    }
}
